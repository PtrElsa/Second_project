fun main() {
    //ArrayOf
    var bahanUatamaSeblak = arrayOf("kerupuk","air","bumbu","sosis")
    println(bahanUatamaSeblak[0])
    println(bahanUatamaSeblak[1])
    println(bahanUatamaSeblak[2])
    println(bahanUatamaSeblak[3])

    //IntArrayOf
    var jumlahPesananSeblak = intArrayOf(3,5,6)
    println(jumlahPesananSeblak[1])

    //BooleanArrayOf
    var pesananSeblak = booleanArrayOf(true, false)
    println(pesananSeblak[0])

    //CharArrayOf
    var namaPanggilan = charArrayOf('O','J','E','N')
    print(namaPanggilan[0])
    print(namaPanggilan[1])
    print(namaPanggilan[2])
    print(namaPanggilan[3])

    //FloatArrayOf
    var value : Float = 53.53F
    println(value)

    //DoubleArrayOf
    var radius : Double = 1000000.50000
    println(radius)

}